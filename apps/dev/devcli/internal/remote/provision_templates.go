func wireguardServerConf(cfg *Config, serverPriv, clientPub, wanIface string) string {
	serverIP := strings.TrimSpace(cfg.WireGuard.RemoteTunnelIP)
	if serverIP == "" {
		serverIP = "10.0.0.1"
	}
	clientIP := strings.TrimSpace(cfg.WireGuard.LocalTunnelIP)
	if clientIP == "" {
		clientIP = "10.0.0.2"
	}
	return fmt.Sprintf(`[Interface]
Address = %s/24
ListenPort = %d
PrivateKey = %s
PostUp = iptables -A FORWARD -i %%i -j ACCEPT; iptables -t nat -A POSTROUTING -o %s -j MASQUERADE
PostDown = iptables -D FORWARD -i %%i -j ACCEPT; iptables -t nat -D POSTROUTING -o %s -j MASQUERADE

[Peer]
PublicKey = %s
AllowedIPs = %s/32
`,
		serverIP,
		cfg.Provision.WireGuardPort,
		serverPriv,
		wanIface,
		wanIface,
		clientPub,
		clientIP,
	)
}

func wireguardClientConf(cfg *Config, clientPriv, serverPub string) (string, error) {
	remoteIP := strings.TrimSpace(cfg.WireGuard.RemoteTunnelIP)
	if remoteIP == "" {
		remoteIP = "10.0.0.1"
	}
	endpoint, err := cfg.wireGuardEndpointResolved()
	if err != nil {
		return "", fmt.Errorf("wireguard endpoint: %w", err)
	}
	return fmt.Sprintf(`# Split-tunnel: only the debug server tunnel IP (api/livekit over WG, not whole server).
[Interface]
Address = %s/32
PrivateKey = %s

[Peer]
PublicKey = %s
Endpoint = %s
AllowedIPs = %s/32
PersistentKeepalive = 25
`,
		cfg.WireGuard.LocalTunnelIP,
		clientPriv,
		serverPub,
		endpoint,
		remoteIP,
	), nil
}